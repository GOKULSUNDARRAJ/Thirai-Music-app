package com.Saalai.SalaiMusicApp.Models;

public class TvShowEpisodeDetail {
    private int episodeId;
    private String episodeName;
    private String episodeLogo;
    private String episodeType;
    private String episodeURL;
    private String episodeVideoId;
    private int episodeNo;
    private String episodeDescription;

    // Getters & Setters
    public int getEpisodeId() { return episodeId; }
    public void setEpisodeId(int episodeId) { this.episodeId = episodeId; }

    public String getEpisodeName() { return episodeName; }
    public void setEpisodeName(String episodeName) { this.episodeName = episodeName; }

    public String getEpisodeLogo() { return episodeLogo; }
    public void setEpisodeLogo(String episodeLogo) { this.episodeLogo = episodeLogo; }

    public String getEpisodeType() { return episodeType; }
    public void setEpisodeType(String episodeType) { this.episodeType = episodeType; }

    public String getEpisodeURL() { return episodeURL; }
    public void setEpisodeURL(String episodeURL) { this.episodeURL = episodeURL; }

    public String getEpisodeVideoId() { return episodeVideoId; }
    public void setEpisodeVideoId(String episodeVideoId) { this.episodeVideoId = episodeVideoId; }

    public int getEpisodeNo() { return episodeNo; }
    public void setEpisodeNo(int episodeNo) { this.episodeNo = episodeNo; }

    public String getEpisodeDescription() { return episodeDescription; }
    public void setEpisodeDescription(String episodeDescription) { this.episodeDescription = episodeDescription; }
}
